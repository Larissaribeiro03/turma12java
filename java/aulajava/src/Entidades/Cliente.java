package Entidades;

public class Cliente {

	
	//Atributos da classe
	public String nome;
	public char genero;
	public int idade;
	public int cpf;
	
	
	//Metodos
	public Cliente(String nome, char genero, int idade, int cpf ) {
		
		this.nome = nome;
		this.genero = genero;
		this.idade = idade;
		this.cpf = cpf;
		
		
	}
	
	
	
	
}
